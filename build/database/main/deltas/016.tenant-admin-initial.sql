CREATE TABLE `TENADM_tenant_admin` (
  `TENADM_id` INT UNSIGNED NOT NULL AUTO_INCREMENT,
  `TEN_id` INT UNSIGNED NOT NULL,
  `TENADM_id_hash` VARCHAR(32) NOT NULL,
  `TENADM_email_address` VARCHAR(254) NOT NULL COMMENT 'Unique to Tenant but not to table.',
  `TENADM_firstname` VARCHAR(32) NOT NULL,
  `TENADM_middlename` VARCHAR(32) NULL,
  `TENADM_lastname` VARCHAR(32) NOT NULL,
  `TENADM_created_timestamp` INT UNSIGNED NOT NULL,
  `TENADM_modified_timestamp` INT UNSIGNED NOT NULL,
  PRIMARY KEY (`TENADM_id`),
  UNIQUE INDEX `TENADM_id_hash_UNIQUE` (`TENADM_id_hash` ASC),
  INDEX `fk_TENADM_TEN_idx` (`TEN_id` ASC),
  UNIQUE INDEX `TENADM_email_TEN_UNIQUE` (`TEN_id` ASC, `TENADM_email_address` ASC),
  CONSTRAINT `fk_TENADM_TEN`
  FOREIGN KEY (`TEN_id`)
  REFERENCES `TEN_tenant` (`TEN_id`)
    ON DELETE RESTRICT
    ON UPDATE RESTRICT)
  DEFAULT CHARACTER SET 'utf8' COLLATE utf8_general_ci
  ENGINE = INNODB;