CREATE TABLE `GRP_group` (
  `GRP_id` INT UNSIGNED NOT NULL AUTO_INCREMENT,
  `DIR_id` INT UNSIGNED NOT NULL,
  `GRP_enabled` INT NOT NULL,
  `GRP_id_hash` VARCHAR(32) NOT NULL,
  `GRP_name` VARCHAR(32) NOT NULL,
  `GRP_description` VARCHAR(255) NULL,
  PRIMARY KEY (`GRP_id`),
  UNIQUE INDEX `GRP_id_hash_UNIQUE` (`GRP_id_hash` ASC),
  INDEX `fk_GRP_DIR_idx` (`DIR_id` ASC),
  CONSTRAINT `fk_GRP_DIR`
  FOREIGN KEY (`DIR_id`)
  REFERENCES `DIR_directory` (`DIR_id`)
    ON DELETE RESTRICT
    ON UPDATE RESTRICT)
  DEFAULT CHARACTER SET 'utf8' COLLATE utf8_general_ci
  ENGINE = INNODB;