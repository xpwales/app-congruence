CREATE TABLE `DIR_directory` (
  `DIR_id` INT UNSIGNED NOT NULL AUTO_INCREMENT,
  `TEN_id` INT UNSIGNED NOT NULL,
  `DIR_id_hash` VARCHAR(32) NOT NULL,
  `DIR_enabled` TINYINT NOT NULL,
  `DIR_name` VARCHAR(32) NOT NULL,
  `DIR_description` VARCHAR(255) NULL,
  PRIMARY KEY (`DIR_id`),
  UNIQUE INDEX `DIR_hash_UNIQUE` (`DIR_id_hash` ASC),
  INDEX `fk_DIR_TEN` (`TEN_id` ASC),
  CONSTRAINT `fk_DIR_TEN`
  FOREIGN KEY (`TEN_id`)
  REFERENCES `TEN_tenant` (`TEN_id`)
    ON DELETE RESTRICT
    ON UPDATE RESTRICT )
  DEFAULT CHARACTER SET 'utf8' COLLATE utf8_general_ci
  ENGINE = INNODB;